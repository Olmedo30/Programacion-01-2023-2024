/*
 * Realiza un programa que imprima los números del 1 al 10 utilizando un bucle for.
 */
package Ejercicios;

/**
 * @author Álvaro Olmedo Fernández
 */
public class Ejercicio3 {
    public static void main(String[] args) {
        
        //Realizamos el bucle donde irá sumando de 1 en 1 y a su vez mostrándolo por pantalla hasta llegar a 10.
        for (int i = 1; i <= 10; i++) {
            System.out.println(i);
        }
    }
}
